export const getPromptByContractType = (category: string, contractType: string): string => {
  const categoryData = promptCategories.find(cat => cat.id === category)
  if (!categoryData) return ''
  
  const contract = categoryData.contracts.find(contract => contract.id === contractType)
  return contract?.prompt || ''
}

export const updatePrompt = (category: string, contractType: string, newPrompt: string): boolean => {
  const categoryData = promptCategories.find(cat => cat.id === category)
  if (!categoryData) return false
  
  const contract = categoryData.contracts.find(contract => contract.id === contractType)
  if (!contract) return false
  
  contract.prompt = newPrompt
  return true
}

